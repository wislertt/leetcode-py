import pytest

from leetcode_py import logged_test

from .helpers import assert_walls_and_gates, run_walls_and_gates
from .solution import Solution


class TestWallsAndGates:
    def setup_method(self):
        self.solution = Solution()

    @logged_test
    @pytest.mark.parametrize(
        "rooms, expected",
        [
            (
                [
                    [2147483647, -1, 0, 2147483647],
                    [2147483647, 2147483647, 2147483647, -1],
                    [2147483647, -1, 2147483647, -1],
                    [0, -1, 2147483647, 2147483647],
                ],
                [[3, -1, 0, 1], [2, 2, 1, -1], [1, -1, 2, -1], [0, -1, 3, 4]],
            ),
            ([[0, -1], [2147483647, 2147483647]], [[0, -1], [1, 2]]),
            ([[0]], [[0]]),
            ([[-1]], [[-1]]),
            ([[2147483647]], [[2147483647]]),
            ([[0, 2147483647]], [[0, 1]]),
            ([[2147483647, 0]], [[1, 0]]),
            ([[0], [2147483647]], [[0], [1]]),
            ([[2147483647], [0]], [[1], [0]]),
            ([[0, 2147483647, 2147483647]], [[0, 1, 2]]),
            ([[2147483647, 2147483647, 0]], [[2, 1, 0]]),
            ([[0, 2147483647, -1, 2147483647]], [[0, 1, -1, 2147483647]]),
            ([[-1, -1], [-1, -1]], [[-1, -1], [-1, -1]]),
            (
                [[2147483647, 2147483647], [2147483647, 2147483647]],
                [[2147483647, 2147483647], [2147483647, 2147483647]],
            ),
            ([[0, 2147483647, 2147483647, 2147483647]], [[0, 1, 2, 3]]),
            (
                [[0, -1, 0], [2147483647, 2147483647, 2147483647], [2147483647, -1, 2147483647]],
                [[0, -1, 0], [1, 2, 1], [2, -1, 2]],
            ),
            (
                [
                    [2147483647, 2147483647, 2147483647],
                    [2147483647, 0, 2147483647],
                    [2147483647, 2147483647, 2147483647],
                ],
                [[2, 1, 2], [1, 0, 1], [2, 1, 2]],
            ),
        ],
    )
    def test_walls_and_gates(self, rooms: list[list[int]], expected: list[list[int]]):
        result = run_walls_and_gates(Solution, rooms)
        assert_walls_and_gates(result, expected)
